// Type Annotations
/*
 Permiten especificar manualmente el tipo de dato de una variable o constante.
 - Sintaxis: var nombre: TipoDeDato = valor
 - Aumenta la claridad del código y ayuda a evitar errores.
 - Obligatorio cuando declaras una variable sin valor inicial.
 
 Ejemplo:
 let name: String = "Alejosor"
 var age: Int = 23
 var pi: Double = 3.1416
 var isSwiftFun: Bool = true

 También se usa cuando defines la variable pero asignas el valor después:
 var score: Int
 score = 100
*/
