import UIKit

// Strings & Integers

// Strings - Type Str
// Son cadenas de texto las cuales deben estar entre ("_")
var saludos = "Hola, playground"
var despedidas = "Adiós, playground"

// Integers - Type Int
// Son todos lo números enteros
var edad = 38
edad = 20
// Se puede trabajar con números grandes separandolos con "_"
var poblacion = 80_000_000 // 80,000,000

/* Importante:
 Una vez que se asigna un tipo de dato a una variable
 no podemos usar otro tipo de dato en la misma*/
