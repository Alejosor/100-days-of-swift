import UIKit

// Constants
/*
 Son valores que no cambian durante la ejecución del programa.
 - Se declaran usando la palabra clave `let`.
 - Una vez asignado el valor, no puede modificarse.
 - Se recomienda usar constantes por defecto, y variables solo cuando el valor necesite cambiar.
 
 Beneficios:
 - Mayor seguridad en el código, evita modificaciones accidentales.
 - Swift puede optimizar el rendimiento al saber que el valor no cambiará.

 Ejemplo:
 let country = "Perú"
 let birthYear = 2001

 // country = "México" // ❌ Error: Cannot assign to value: 'country' is a 'let' constant

IMPORTANTE = Cuando programemos en Swift, siempre hay que usar let a menos a que queramos cambiar el valor de una variable a posterior.
*/
let nacionalidad = "Perú"
let idioma = "Español"
